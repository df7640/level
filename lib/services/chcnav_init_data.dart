///   init59: btsnoop 캡처 59개 고유 명령 (seq=0x00, trailing=0x00)
///   init29: 원본 29개 명령 (원래 seq/trailing 유지)
///   init25: init59 중 IN_DART 매칭 25개
///   init99: init59 + 고빈도 폴링 명령 반복 (~89-99개)
