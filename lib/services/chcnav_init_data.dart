  static List<List<int>> getCommands(InitMode mode) {
    switch (mode) {
      case InitMode.init59:
        return _init59;
      case InitMode.init29:
        return _init29;
      case InitMode.init25:
        return _init25;
      case InitMode.init99:
        return _buildInit99();
      case InitMode.skipInit:
        return [];
    }
  }

  /// init59 중 고빈도 폴링 명령을 반복하여 ~89-99개 생성
  /// count 1x → 1x, count 2-4x → 2x, count 34+ → 3x
  static List<List<int>> _buildInit99() {
    final result = <List<int>>[];
    for (int i = 0; i < _init59.length; i++) {
      final count = _init59RepeatCount[i] ?? 1;
      int repeats;
      if (count <= 1) {
        repeats = 1;
      } else if (count <= 4) {
        repeats = 2;
      } else {
        repeats = 3;
      }
      for (int r = 0; r < repeats; r++) {
        result.add(_init59[i]);
      }
    }
    return result;
  }

